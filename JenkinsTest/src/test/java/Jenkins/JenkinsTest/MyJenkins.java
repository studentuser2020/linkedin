package Jenkins.JenkinsTest;

public class MyJenkins {

	public static void main(String[] args) {
		
		//print 1 to 10
		for (int i=1;i<=10;i++) {
			System.out.println("i vlaue is--> "+ i);
		}
		
		
		//print reverse 10 to 1
		for (int k=10;k>=1;k--) {
			System.out.println("k vlaue is--> "+ k);
	
		}

	}
	
}
